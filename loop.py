#LOOPING IN PYTHON 
colors = ["red", "green", "blue", "purple"]
i = 0
while i < len (colors):
	print(colors[i])
	i += 1


# Range of Length 
colors = ["red", "green", "blue", "purple"]
for i in range(len(colors)):
	print(colors[i])


# For-in : the usual way 
colors = ["red", "green", "blue","purple"]
for color in colors:
	print(color)


cars = ["Mazda", "Tesla", "Benz", "Toyota", "Honda"]
for car in cars:
	print(car)